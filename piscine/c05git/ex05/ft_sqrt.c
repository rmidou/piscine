int	ft_sqrt(int nb)
{
	int	i;
	int	compteur;

	compteur = 0;
	i = 1;
	while (nb > 0)
	{
		nb -= i;
		i += 2;
		compteur++;
	}
	if (nb == 0)
		return (compteur);
	return (0);
}
